17. FINALIDAD DEL TRATAMIENTO DE DATOS PERSONALES
</p>
<p>
El consentimiento es la base para el tratamiento de los datos personales y debe ser dado libremente, específico, informado, inequívoco y otorgado bajo información sólida que la Institución Educativa garantiza en todo momento. Su consentimiento es nuestra legitimación para el tratamiento de datos.
</p>
<p>
Para llevar a cabo las tareas ordinarias para el personal, los estudiantes y los padres, la Institución Educativa puede procesar una amplia gama de datos personales sobre las personas (incluido el personal actual, pasado y futuro, los estudiantes o los padres) como parte de su funcionamiento diario. Parte de esta actividad debe realizarse para el cumplimiento de sus derechos, deberes u obligaciones, incluidos los desarrollados en virtud de un contrato con su personal, los padres o los propios alumnos si se encuentran emancipados.
</p>
<p>
Otros usos de los datos personales se realizarán de acuerdo con los intereses legítimos de la Institución Educativa, o los intereses legítimos de otro, siempre que estos no se vean compensados por el impacto en las personas y siempre que no impliquen tipos de datos especiales o sensibles. la Institución Educativa espera que los siguientes usos puedan caer dentro de esa categoría de "intereses legítimos":
</p>
<p>


Para los propósitos de la selección de estudiantes (y para confirmar la identidad de los futuros estudiantes y sus padres);
Las finalidades descritas en el Contrato de Matrícula.
Proporcionar servicios de educación, entrenamiento físico o desarrollo espiritual, servicios profesionales, actividades extracurriculares y monitorear el progreso y las necesidades educativas de los estudiantes;
Prestar servicios de transporte escolar, catering, atención especializada, etc;
Mantener relaciones con el estudiante y la comunidad escolar, incluida la actividad de marketing directo o de recaudación de fondos.
A los efectos de la debida diligencia de los donantes y para confirmar la identidad de los posibles donantes y sus antecedentes e intereses relevantes;
A los efectos de la planificación y la previsión de la gestión, la investigación y el análisis estadístico, incluidos los impuestos o previstos por la ley (como el análisis de diferencias salariales de diversidad o de género y los registros tributarios);
Permitir a las autoridades pertinentes monitorear el desempeño de la Institución Educativa e intervenir o ayudar con los incidentes según corresponda;
Dar y recibir información y referencias sobre estudiantes pasados, actuales y futuros, hacia / desde cualquier institución educativa a la que asistió el alumno o donde se propone que asista; y proporcionar referencias a posibles empleadores de estudiantes anteriores;
Permitir a los estudiantes participar en evaluaciones nacionales o de otro tipo y publicar los resultados de exámenes públicos u otros logros de los estudiantes de la Institución Educativa;
Para salvaguardar el bienestar de los estudiantes y proporcionar un cuidado pastoral apropiado;
Cumplir con las obligaciones contractuales y legales de la Institución Educativa;
Monitorear (según corresponda) el uso de las TIC y los sistemas de comunicaciones de la Institución Educativa.
Hacer uso de imágenes fotográficas de los estudiantes en las publicaciones de la Institución Educativa en su sitio web y (en su caso) en las redes sociales;
Por motivos de seguridad, incluido CCTV;
Para el cobro de cartera pendiente derivada de los servicios prestados por la Institución Educativa
Además de los usos antes mencionados, en cuanto al personal de la Institución Educativa para la selección y reclutamiento, procesos, antecedentes profesionales, evaluación de idoneidad y calificación para el puesto, antecedentes penales, contratación, nómina, inscripciones y bajas en los registros de personal, etc. Y
Cuando sea razonablemente necesario para los propósitos de la Institución Educativa, incluso para obtener asesoramiento profesional adecuado y seguro.

</p>
<p>
Además, la Institución Educativa puede necesitar procesar una categoría especial de datos personales (relacionados con la salud, estado mental etc.) o información de antecedentes penales de acuerdo con los derechos u obligaciones que le impone la ley, incluso en protección y empleo, o de vez en cuando por consentimiento explícito cuando sea necesario. Estas razones pueden incluir:
</p>
<p>
Para salvaguardar el bienestar de los estudiantes y proporcionar la atención pastoral apropiada (y cuando sea necesario, médica) y tomar las medidas apropiadas en caso de una emergencia, incidente o accidente, incluso mediante la divulgación de detalles de la condición médica de una persona cuando sea de su interés para hacerlo, por ejemplo, con fines de asesoramiento médico, servicios sociales, seguros u organizadores de viajes escolares;
Para proporcionar servicios educativos en el contexto de cualquier necesidad educativa especial de un alumno;
Proporcionar educación espiritual en el contexto de cualquier creencia religiosa;
En relación con el empleo de su personal, por ejemplo, planes de bienestar o pensiones;
Para fines legales y reglamentarios (por ejemplo, protección infantil, monitoreo de la diversidad y salud y seguridad) y para cumplir con las obligaciones legales y deberes de cuidado.
</p>
