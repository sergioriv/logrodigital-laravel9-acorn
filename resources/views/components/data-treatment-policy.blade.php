13. PROHIBICIONES
</p>
<p>
En desarrollo de esta norma de seguridad de la información personal, se establecen las siguientes prohibiciones y sanciones como consecuencia de su incumplimiento.
</p>
<p>
la Institución Educativa prohíbe el acceso, uso, gestión, cesión, comunicación, almacenamiento y cualquiera otro Tratamiento de datos personales de carácter sensible sin autorización del Titular del dato personal y/o de la Institución Educativa.
la Institución Educativa prohíbe a los destinatarios de esta Norma cualquier Tratamiento de datos personales que pueda dar lugar a alguna de las conductas descritas en la Ley de delitos informáticos (Ley 1273 de 2009). Salvo que se cuente con la autorización del Titular del dato y/o de la Institución Educativa según el caso.
la Institución Educativa prohíbe el Tratamiento de datos personales de niños y adolescentes menores de edad. Todo Tratamiento que se llegare a hacer respecto de los datos de los menores, se deberán asegurar los derechos prevalentes que la Constitución Política reconoce a estos, en armonía con el Código de la Infancia y la Adolescencia. En casos de Tratamiento de estos datos la autorización deberá ser otorgada por los representantes legales, según el caso.


14. PROCEDIMIENTO PARA EL EJERCICIO DE LOS DERECHOS DEL TITULAR DE DATOS.
</p>
<p>
Los derechos de los Titulares establecidos en la Ley 1581 de 2012, podrán ser ejercidos ante la Institución Educativa por las siguientes personas:
</p>
<p>
Por el Titular de los datos, quien deberá acreditar ante la Institución Educativa su identidad en forma suficiente por los distintos medios o mecanismos que tenemos a su disposición
Por los causahabientes del Titular de los datos, quienes deberán acreditar tal calidad ante la Institución Educativa.
Por el representante y/o apoderado del Titular de los datos, previa acreditación ante la Institución Educativa de la representación o apoderamiento.
Por estipulación a favor de otro o para otro. De acuerdo con lo previsto en la ley 1581, en sus artículos 14 y 15, para el ejercicio de cualquiera de los derechos que le asisten como Titular de los datos, usted podrá utilizar ante la Institución Educativa cualquiera de los mecanismos que se establecen a continuación:
</p>
<p>

A. Procedimiento para Consultas: Los Titulares, sus causahabientes, sus representantes o apoderados, podrán consultar la información personal del Titular que repose en las bases de datos de la Institución Educativa.
</p>
<p>


La Institución Educativa como Responsable y/o Encargado del Tratamiento de Datos suministrará la información solicitada que se encuentre contenida la base de datos o la que esté vinculada con la identificación del Titular. El Titular acreditará su condición mediante copia del documento pertinente y de su documento de identidad que podrá suministrar en medio físico o digital, en caso de que el Titular esté representado por un tercero, deberá allegarse el respectivo poder, que deberá contener el respectivo contenido ante notario, el apoderado deberá igualmente acreditar su identidad en los términos indicados. La consulta se formulará a través de los canales que para dicho efecto han sido habilitados por la Institución Educativa y en especial a través de comunicación escrita o electrónica, dirigida a la dependencia y persona indicada en el presente Manual. La consulta será atendida por la Institución Educativa en un término máximo de diez (10) días hábiles contados a partir de la fecha de recibo de la misma. Cuando no fuere posible para la Institución Educativa atender la consulta dentro de dicho término, lo informará al interesado, expresando los motivos de la demora y señalando la fecha en que atenderá su consulta, la cual en ningún caso superará los cinco (5) días hábiles siguientes al vencimiento del primer término.
</p>
<p>


Se podrán consultar de forma gratuita los datos personales al menos una vez cada mes calendario, y cada vez que existan modificaciones sustanciales de las Políticas establecidas en este Manual que motiven nuevas consultas.
</p>
<p>


Para consultas cuya periodicidad sea mayor a una por cada mes calendario, la Institución Educativa podrá cobrar al Titular los gastos de envío, reproducción y, en su caso, certificación de documentos.


</p>
<p>
B. Procedimiento para Reclamos: Los Titulares, sus representantes o apoderados, que consideren que la información que se encuentra contenida en las Bases de Datos de la Institución Educativa deben ser objeto de corrección, actualización o supresión, o cuando adviertan el presunto incumplimiento de cualquiera de los deberes contenidos en la ley, podrán presentar un reclamo ante la Institución Educativa como Responsable y/o Encargada del Tratamiento, el cual será tramitado bajo las siguientes reglas:
</p>
<p>
El reclamo se formulará mediante solicitud escrita dirigida al Colegio, con la identificación del Titular, la descripción de los hechos que dan lugar al reclamo, la dirección, y acompañando los documentos que se quiera hacer valer. Al reclamo deberá adjuntarse fotocopia del documento de identificación del Titular de los datos.
El reclamo se formulará a través de los canales que para dicho efecto han sido habilitados por la Institución Educativa, y se dirigirá a la dependencia y a la persona indicada en la presente política.
Si el reclamo resulta incompleto, la Institución Educativa requerirá al interesado dentro de los cinco (5) días hábiles siguientes a la recepción del reclamo para que subsane las fallas. Transcurridos dos (2) meses desde la fecha del requerimiento realizado por la Institución Educativa, sin que el solicitante presente la información requerida, la Institución Educativa entenderá que se ha desistido del reclamo. En caso de que quien reciba el reclamo no sea competente para resolverlo, dará traslado a quien corresponda en un término máximo de dos (2) días hábiles e informará de la situación al interesado.
Una vez la Institución Educativa reciba el reclamo completo, incluirá en la base de datos una leyenda que indique: "reclamo en trámite" y el motivo del mismo, en un término no mayor a dos (2) días hábiles. Dicha leyenda deberá mantenerse hasta que el reclamo sea decidido.
o El término máximo para atender el reclamo por parte de la Institución Educativa será de quince (15) días hábiles contados a partir del día siguiente a la fecha de su recibo.

o Cuando no fuere posible para la Institución Educativa atender el reclamo dentro de dicho término, se informará al interesado los motivos de la demora y la fecha en que se atenderá su reclamo, la cual en ningún caso podrá superar los ocho (8) días hábiles siguientes al vencimiento del primer término.



