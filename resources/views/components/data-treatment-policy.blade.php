<b>INTRODUCCIÓN</b>
<p>
Con el fin de dar cumplimiento a la legislación vigente en materia de protección de datos, en especial a la Ley 1581 de 2012 (y demás normas que la modifiquen, adicionen, complementen o desarrollen) al Decreto 1377 de 2013 y al Decreto Reglamentario 1074 de 2015, a continuación, lo ponemos al tanto de los aspectos relevantes en relación con la recolección, uso, tratamiento y transferencia de datos personales que la Institución Educativa, realiza de sus datos personales, en virtud de la autorización otorgada por usted para adelantar dicho tratamiento, así como también del manejo de los mismos.
</p>
<p>
El presente documento establece las Políticas de Tratamiento de Datos Personales (en adelante la “Política”) de la Institución Educativa, en cumplimiento de las disposiciones de la Ley 1581 de 2012, el Decreto 1377 de 2013 y en el Decreto Reglamentario 1074 de 2015, y en él se describen los mecanismos por medio de los cuales la Institución Educativa garantiza un manejo adecuado de los datos personales recolectados en sus bases de datos, con el fin de permitir a los titulares el ejercicio del derecho de Habeas Data.
</p>
<p>
Conforme a lo previsto en el artículo 15 de la Constitución Política de Colombia y la legislación aplicable (Ley 1266 de 2008, Ley 1581 de 2012, Decreto 1377 de 2013 y todas aquellas normas que las reglamenten, adicionen, deroguen o modifiquen), tenemos una clara política de privacidad y protección de sus datos personales.
</p>


2. <b>OBJETO</b>
<br>
<p>
la Institución Educativa tiene como uno de los principales valores la privacidad y confidencialidad de todas las personas, especialmente aquellas cuyos datos personales procesamos. Dentro de ese compromiso, la protección de los datos personales de nuestros estudiantes es fundamental.
</p>
<p>
De acuerdo con los criterios anteriores, el propósito de esta política es brindarle información sobre qué, cómo y por qué recopilamos su información personal, cómo podemos usar esa información y con quién podemos compartirla y bajo qué causas. Nuestra Política de privacidad también describe las medidas que tomamos para proteger la seguridad de su información. También le informamos cómo puede comunicarse con nosotros para obtener respuestas a las preguntas que pueda tener sobre nuestras prácticas de privacidad y cómo puede ejercer sus derechos sobre sus datos personales.
</p>
<p>
La Institución Educativa está fuertemente comprometida con el cumplimiento de la Ley 1581 de 2012, el Decreto 1377 de 2013 y el Decreto Reglamentario 1074 de 2015 así como el resto de las normas aplicables en materia de protección y educación infantil entre otros.
