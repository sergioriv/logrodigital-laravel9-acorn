5. <b>AMBITO DE APLICACIÓN</b>
</p>
<p>
El ámbito de aplicación de esta Política de conformidad con la Ley 1581 de 2012, serán los datos de personas naturales registrados en todas las bases de datos de propiedad de la Institución Educativa o cuyo Tratamiento ha sido encargado a éstas.

La presente Política aplicará al Tratamiento de Datos Personales efectuado en territorio colombiano o cuando la Institución Educativa en su calidad de Responsable o Encargado del Tratamiento de los Datos, dejare de estar domiciliada en el territorio nacional, sin embargo, le sea aplicable la legislación colombiana en virtud de normas y tratados internacionales.

El régimen de protección de datos personales que se establece en el presente Manual no será de aplicación cuando:

Las bases de datos y archivos que tengan por finalidad la seguridad y defensa nacional, así como la prevención, detección, monitoreo y control del lavado de activos y el financiamiento del terrorismo.
Las bases de datos que tengan como fin y contengan información de inteligencia y contrainteligencia.
Las bases de datos y archivos de información periodística y otros contenidos editoriales.
Información requerida por una entidad pública o administrativa en ejercicio de sus funciones legales o por orden judicial;
Datos de naturaleza pública;
Casos de urgencia médica o sanitaria;
Tratamiento de información autorizado por la ley para fines históricos, estadísticos o científicos;
Datos relacionados con el Registro Civil de las Personas.
Las bases de datos y archivos regulados por la Ley 1266 de 2008.
Las bases de datos y archivos regulados por la Ley 79 de 1993.
</p>
