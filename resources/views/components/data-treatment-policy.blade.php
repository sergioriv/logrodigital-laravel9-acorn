28. APLICACIONES EDUCATIVAS
</p>
<p>
la Institución Educativa utiliza un sistema de información de gestión escolar así como diferentes plataformas educativas y de aprendizaje para complementar la enseñanza (servicios de computación en la nube). Estas plataformas almacenan y procesan datos sensibles de padres, profesores y alumnos estrictamente para fines internos y educativos. Los profesores solicitan, antes de su uso, autorización de la Institución Educativa. Cada solicitud de autorización implica la valoración de la solicitud desde el punto de vista de la protección de datos y seguridad de la información así como la posterior autorización o denegación por parte de la Institución Educativa.
</p>
<p>
Todos los usuarios de estas plataformas tienen acceso restringido mediante convenientes contraseñas.
</p>
<p>
El uso de dichas plataformas educativas no implica en ningún momento la transmisión de los datos del alumno al proveedor de servicios de la aplicación para que pueda utilizar dichos datos para sus propios fines o almacenar los datos de forma permanente. la Institución Educativa siempre conservará su derecho a acceder a los datos del estudiante y su eliminación cuando lo considere apropiado.
</p>
<p>
Estas plataformas cumplen estrictamente la legislación de protección de datos y aseguran la adopción de garantías suficientes en caso de que se produzca una transferencia internacional de datos.
</p>
<p>
El uso de dichas plataformas educativas no implica en ningún momento la transmisión de los datos del alumno al proveedor del servicio de la aplicación para que éste pueda utilizarlo para sus propios fines o almacenar los datos de forma permanente. la Institución Educativa siempre conservará su derecho de acceso a los datos y su eliminación cuando lo considere apropiado.
</p>
<p>
Esta política de protección de datos se revisa periódicamente para asegurar el cumplimiento de la normativa vigente en Protección de Datos.


