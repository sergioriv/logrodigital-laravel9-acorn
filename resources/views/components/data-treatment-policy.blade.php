la Institución Educativa debe cumplir con la legislación de privacidad y esta política. Esta política se revisará y actualizará periódicamente para tener en cuenta las nuevas leyes vigentes, las nuevas tecnologías y el cambio del entorno escolar cuando sea necesario. Asegúrese de haber leído la versión actual de esta política.
