21. TRANSFERENCIA Y TRASMISION DE LA INFORMACIÓN.
</p>
<p>
Para el cumplimiento del objeto social de la Institución Educativa se hace indispensable que usted suministre información y datos personales al Colegio, justamente para que éste pueda prestar los servicios educativos, de cafetería, transporte escolar y demás servicios adicionales o conexos. Por ello, usted autoriza de manera previa, expresa y informada al Colegio para hacer entregar de dicha información personal a terceros (personas naturales y jurídicas) que se encargan de prestar servicios y de suministrar bienes al Colegio.
