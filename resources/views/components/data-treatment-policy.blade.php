El sitio web de la Institución Educativa rastrea los patrones de navegación web para comprender mejor cómo se utiliza nuestro sitio web. Esta información genérica se recopila mediante el uso de cookies de sesión. Las cookies utilizadas por nuestros sitios web están asociadas con un usuario anónimo y su computadora, y no están relacionadas con los datos personales del usuario.
