6. <b>TÉRMINOS Y DEFINICIONES:</b>
</p>
<p>
Autorización: Consentimiento previo, expreso e informado del Titular para llevar a cabo el Tratamiento de datos personales;
Base de Datos: Conjunto organizado de datos personales que sea objeto de Tratamiento;
Dato personal: Cualquier información relacionada con un individuo o "Interesado", que se puede utilizar para identificar directa o indirectamente a la persona. Puede ser cualquier dato, desde un nombre o dirección hasta una fotografía&nbsp;  e incluye uno o más factores relacionados con la salud y la identidad económica, cultural o social de una persona.
Datos biométricos: Datos personales que resultan del procesamiento de datos específicos relacionados con características físicas y de comportamiento de una persona, que permiten la identificación de esa persona (por ejemplo, firmas).
Datos genéticos: Datos relacionados con las características genéticas de una persona física, ya sean inherentes o adquiridos, que ofrecen información sobre la salud mental o física de esa persona.
Datos relativos a la salud: Datos personales referidos a la salud física y mental personal de una persona, incluida información sobre los servicios de salud a los que se accede.
Datos personales sensibles: Aquellos datos que afectan la intimidad del Titular o cuyo uso indebido puede generar su discriminación;
Encargado de tratamiento: Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, realice el Tratamiento de datos personales por cuenta de la Institución Educativa como Responsable de los datos. Los encargados del tratamiento de datos incluyen servicios psicológicos entre otros.
Responsable del tratamiento: Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, decida sobre la base de datos y/o el Tratamiento de los datos.
Titular: Persona natural cuyos datos personales sean objeto de Tratamiento
Trabajador: Toda persona natural que preste un servicio al Colegio en virtud de un contrato laboral.
Estudiante: Toda persona natural que esté vinculado a la Institución Educativa en virtud de un documento físico o digital de matrícula.
Padre de Familia: Toda persona natural que sea Representante Legal y/o económico de un estudiante.
Transferencia: Se refiere al envío por parte de la Institución Educativa como Responsable del Tratamiento o un Encargado de los datos, a un tercer agente o persona natural/jurídica (receptor), dentro o fuera del territorio nacional para el tratamiento efectivo de datos personales;
Trasmisión: Se refiere a la comunicación de datos personales por parte del Responsable al Encargado, ubicado dentro o fuera del territorio nacional, para que el Encargado, por cuenta del Responsable, trate datos personales;
Tratamiento: Cualquier operación o conjunto de operaciones sobre datos personales, tales como la recolección, almacenamiento, uso, circulación o supresión.</p>
