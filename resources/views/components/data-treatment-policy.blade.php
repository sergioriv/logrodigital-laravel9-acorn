23. CONSENTIMIENTO PARA USAR SU IMAGEN
</p>
<p>
la Institución Educativa enfatiza la importancia de celebrar el éxito de los alumnos y, como tal, las fotos y videos de nuestros alumnos se utilizan a menudo para mostrar sus habilidades y talentos, así como la vida cotidiana de la Institución Educativa.
</p>
<p>
La Institución Educativa está comprometido con la protección de la imagen de nuestros alumnos y nos tomamos muy en serio el cumplimiento de la normativa de protección de datos, especialmente en lo que respecta al consentimiento para la publicación de contenidos audiovisuales y la protección de la confidencialidad y privacidad de nuestros alumnos, personal y otras personas que pueden aparecer en nuestras publicaciones.
</p>
<p>
Es por ello que, al comienzo del año escolar, la Institución Educativa solicita autorización para compartir los contenidos audiovisuales de los estudiantes en los sitios web, marketing, canales de redes sociales, prensa y actividades extracurriculares. Los estudiantes mayores de edad pueden dar su consentimiento por sí mismos sin autorización de los padres sin perjuicio de la supervisión de los padres.
</p>
<p>
La Institución Educativa nunca difundirá ninguna fotografía o video sin el consentimiento previo de los padres o del interesado.
</p>
<p>
La Institución Educativa cuenta con un fotógrafo oficial que asiste a eventos especiales en la Institución Educativa y que también cumple con los requisitos de la legislación.
</p>
<p>
Por otro lado, si bien está permitido que los padres tomen fotografías de sus hijos en eventos especiales y organizados y celebraciones dentro de las instalaciones de la Institución Educativa, la Institución Educativa no se hace responsable del uso que los padres hagan de las imágenes ni de reclamaciones de terceros que puede aparecer en las fotografías de los padres sin su consentimiento.
</p>
<p>
Además, la Institución Educativa no autoriza el uso de los contenidos de sus publicaciones por parte de terceros ni es responsable de las responsabilidades que puedan derivarse para estos terceros de estos usos no autorizados.
</p>
<p>
En los casos en que se utilice la imagen del alumno con fines publicitarios específicos, la Institución Educativa, a través de los servicios centrales de marketing, remitirá a los padres la información correspondiente sobre el uso de las imágenes y su duración así como una muestra de las fotografías a utilizar, asegurando su consentimiento. De manera similar, su consentimiento se recopilará anualmente para la toma y uso de la imagen de su hijo para el anuario escolar.
</p>
<p>
En relación a las actividades realizadas fuera de la Institución Educativa, se requiere el consentimiento de los padres o alumnos mayores de edad cuando estas actividades no se realicen en el ejercicio de la función educativa.
</p>
<p>
Cuando la grabación o toma de fotografías sea realizada por terceros, es decir, por los responsables de la actividad o institución que es visitada por los estudiantes, será obligación de estos terceros obtener el consentimiento a través de la Institución Educativa.
</p>
