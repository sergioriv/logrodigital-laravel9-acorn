<p>
<b>Política de Tratamiento de Datos. Autorización de uso de derechos de imagen</b>
</p>
<p>
Autorización de uso de derechos de imagen sobre fotografías y producciones audiovisuales (videos) y de propiedad intelectual otorgado a la Institución Educativa.
</p>
<p>
Mediante el presente documento, formato autorizo a la Insitución Educativa para que haga el uso y tratamiento de mis derechos de imagen para incluirlos sobre fotografías y producciones audiovisuales (videos); así como de los Derechos de Autor; los Derechos Conexos y en general todos aquellos derechos de propiedad intelectual que tengan que ver con el derecho de imagen.
</p>
<p>
Esta autorización se regirá por las normas legales aplicables y en particular por las siguientes:
</p>
<p>
Este video/foto podrá ser utilizado con fines educativos e informativos en diferentes escenarios y plataformas de la Insitución Educativa.
</p>
<p>
Este video/foto es sin ánimo de lucro y en ningún momento será utilizado para objetivos distintos. La Institución Educativa queda exenta de cualquier responsabilidad que se pueda derivar de la presente actividad con la firma de la autorización.
</p>
<p>
La presente autorización no tiene ámbito geográfico determinado, por lo que las imágenes en las que aparezca podrán ser utilizadas en el territorio del mundo, así mismo, tampoco tiene ningún límite de tiempo para su concesión, ni para explotación de las imágenes, o parte de estas, por lo que mi
autorización se considera concedida por un plazo de tiempo ilimitado.
</p>
